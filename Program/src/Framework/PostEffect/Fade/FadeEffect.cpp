post_effect::FadeEffect::FadeEffect() : PostEffect("fade") {
}

//------------------------------------------------
// dtor
//------------------------------------------------
post_effect::FadeEffect::~FadeEffect() {
}

//------------------------------------------------
// Process
//------------------------------------------------
void post_effect::FadeEffect::_Process(LPDIRECT3DTEXTURE9 p_source) {
  auto p_shader = this->GetShader();
  p_shader->SetFloat("u_fade_value", GetAlpha());
  p_shader->SetTexture("texture_decale", p_source);
}
